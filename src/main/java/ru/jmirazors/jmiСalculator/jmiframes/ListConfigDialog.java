    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Сохранить
        if (jCheckBox1.isSelected())
            properties.put("showDel", "yes");
        else
            properties.put("showDel", "no");
        properties.put("contragent", jTextField1.getText());
        properties.put("user", jTextField2.getText());
        properties.put("firstDate", format.format(jDateChooser1.getDate()));
        if (jCheckBox2.isSelected())
            properties.put("lastDate", "curdate");
        else
            properties.put("lastDate", format.format(jDateChooser2.getDate()));
        properties.put("rowCount", jFormattedTextField1.getText());
        dispose();
    }//GEN-LAST:event_jButton4ActionPerformed

