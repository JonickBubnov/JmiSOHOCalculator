    public ListConfigDialog(java.awt.Frame parent, boolean modal, Map prop) {
        super(parent, modal);
        this.properties = prop;
        initComponents();
            
            if (properties.get("showDel").equals("yes"))
                jCheckBox1.setSelected(true);
            else
                jCheckBox1.setSelected(false);
            jTextField1.setText(properties.get("contragent").toString());
            contrName.append(properties.get("contragent"));
            jTextField2.setText(properties.get("user").toString());
            usrName.append(properties.get("user"));
        try {
            jDateChooser1.setDate(format.parse(properties.get("firstDate").toString()));                            
        } catch (ParseException ex) {
            jDateChooser1.setDate(new Date());
        }
        try {
            if (properties.get("lastDate").toString().equals("curdate")) {
                jDateChooser2.setDate(new Date());
                jDateChooser2.setEnabled(false);
                jCheckBox2.setSelected(true);                
            } else {
                jDateChooser2.setDate(format.parse(properties.get("lastDate").toString())); 
                jDateChooser2.setEnabled(true);
                jCheckBox2.setSelected(false);
            }
        } catch (ParseException ex) {
            jDateChooser2.setDate(new Date());
        }        
        jFormattedTextField1.setText(properties.get("rowCount").toString());
        
    }

